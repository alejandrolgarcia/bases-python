# Tipos de datos en Python
a = "Hello Word!"
print(type(a))
b = 20
print(type(b))
c = 20.51
print(type(c))
d = 1j
print(type(d))
# list
e = ["apple", "banana", "cherry"]
print(type(e))
# tuple
f = ("apple", "banana", "cherry")
print(type(f))
# range
g = range(6)
print(type(g))
# dict
h = {"name" : "John", "age" : 36}
print(type(h))
# set
i = {"apple", "banana", "cherry"}
print(type(i))
# frozenset
j = frozenset({"apple", "banana", "cherry"})
print(type(j))
# bool
k = True
print(type(k))




